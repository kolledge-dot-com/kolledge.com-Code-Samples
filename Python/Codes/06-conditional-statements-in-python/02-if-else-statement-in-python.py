# Control flow for if....else statement
x = 10
if x > 5:
    print("I am the code-block-If")
    print("Logical condition is True")
    print()
else:
    print("I am the code-block-Else")
    print("Logical condition is False")
    print()

print("I am the outer-code-block.")




# Bitwise and operator in python
x = 10
y = 22
res = x & y
print(res)


# Bitwise or operator in python
x = 10
y = 22
res = x | y
print(res)


# Bitwise xor operator in python
x = 10
y = 22
res = x ^ y
print(res)


# Bitwise not operator in python
y = 22
res = ~y
print(res)


# Bitwise left-shift operator in python
x = 5
y = 2
res = x << y
print(res)


# Bitwise right-shift operator in python
x = 50
y = 2
res = x >> y
print(res)



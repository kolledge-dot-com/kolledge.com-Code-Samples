# In membership operator
x = 10
y = 100
ch = "F"
seq = [10, 20, 30]
str_seq = "ABCDEF"

res_x = x in seq
res_y = y in seq
res_ch = ch in str_seq

print(res_x)
print(res_y)
print(res_ch)



# Not In membership operator
x = 10
y = 100
ch = "F"
seq = [10, 20, 30]
str_seq = "ABCDEF"

res_x = x not in seq
res_y = y not in seq
res_ch = ch not in str_seq

print(res_x)
print(res_y)
print(res_ch)
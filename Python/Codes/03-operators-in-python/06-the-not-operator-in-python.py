# If the operand is True it returns False
x = True
res = not x
print(res)


# If the operand is False it returns True
x = False
res = not x
print(res)


# If the operand is a nonzero arithmetic value
# it returns False
x = 100
res = not x
print(res)


# If the operand of not operator is zero it returns True
x = 0
res = not x
print(res)


# simplest form of print statement
print(3)


# print multiple values, separated by commas
print(3, 4, 5)


# print a string
print("Hello, world!")


# concatenate two or more strings while printing
print ("Hello " + "world!")


# print the results of mathematical expressions
print (3 + 4)


# print the contents of variables
x = 5
print (x)


# Print End Argument
print("Hello, world!", end=" - ")
print("How do you do?")


# Print Sep Argument
print("Hello, world!", "How do you do?", sep=" - ")

